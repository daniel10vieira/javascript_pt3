/*
    Switch Case - Controlador de Fluxo
*/

const temperature = 20;

switch (temperature) {
  case 10:
    console.log("Tá meio frio.")
    break;

  case 20: 
    console.log("Está na média.")
    break;

  case 30:
    console.log("Está muito quente!")
    break;

  default:
    console.log("Não sei te dizer.")
    break;
}
